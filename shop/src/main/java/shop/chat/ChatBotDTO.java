package shop.chat;

public class ChatBotDTO {
	private int cbnum;
	private int ccnum;
	private String cbq;
	private String cba;
	
	public int getCbnum() {
		return cbnum;
	}
	public void setCbnum(int cbnum) {
		this.cbnum = cbnum;
	}
	public int getCcnum() {
		return ccnum;
	}
	public void setCcnum(int ccnum) {
		this.ccnum = ccnum;
	}
	public String getCbq() {
		return cbq;
	}
	public void setCbq(String cbq) {
		this.cbq = cbq;
	}
	public String getCba() {
		return cba;
	}
	public void setCba(String cba) {
		this.cba = cba;
	}
}
